function Weapon(name, damage, id, type) {

    this.name = name;
    this.damage = damage;
    this.defence = 10;
    this.id = id;
    this.type = type;

}

function Defence(name) {
    this.name = name;
    this.defence = 10
}


function boostAttack(name) {
    this.name = name;
    this.damage = 10
}


function Health(name) {
    this.name = name;
    this.health = 10
}

